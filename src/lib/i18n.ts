export type TranslationSet = (typeof TRANSLATIONS)[Locale];

type CookieStore = {
  get: (name: string) => { value: string } | undefined;
};

export const normalizeLocale = (value?: string | null): Locale => {
  if (!value) {
    return DEFAULT_LOCALE;
  }
  const normalized = value.toLowerCase();
  return SUPPORTED_LOCALES.includes(normalized as Locale)
    ? (normalized as Locale)
    : DEFAULT_LOCALE;
};

export const getLocaleFromCookies = (cookieStore?: CookieStore | null) => {
  if (!cookieStore) {
    return DEFAULT_LOCALE;
  }
  const value = cookieStore.get(LOCALE_COOKIE)?.value ?? null;
  return normalizeLocale(value);
};

export const getTranslations = (locale: Locale): TranslationSet =>
  TRANSLATIONS[locale] ?? TRANSLATIONS[DEFAULT_LOCALE];
